package javaLearningbasics;

public class Primitive_DataType_27_1 {

	public static void main(String[] args) {
		
		System.out.println("data types");
		
		// most used primitive data types
		
				int i=10;                       // which holds a whole number  with bigger values
				double d=20.5;                  //which holds decimal with bigger values with 15 decimal digits
				char c='H';                    //which holds single character or ASCII values
				boolean b= true;               //which holds true or false
				
			// other primitive data types are
				 
				float f=20.3f;               // which holds smaller value of decimal like 6-7 decimal digits
				short s=32767;               // which holds a whole number with medium values
				byte  by=127;                // which holds a whole number with smaller values 
			    long  l=92222;	             // which holds a whole number with very bigger values
			    
				//declaration(data type)variable=value
				
				 int x; //declaration            // u can also write int x=100
				 x=100; // intialization
				 System.out.println("x: " +x);
				 System.out.println("i: " +i);
				 System.out.println("c: " +c);
				 System.out.println("b: " +b);
				 System.out.println("d: " +d);
				 System.out.println(f);
				 System.out.println(s);
				 System.out.println(by);
				 System.out.println(l);				
	}
}

